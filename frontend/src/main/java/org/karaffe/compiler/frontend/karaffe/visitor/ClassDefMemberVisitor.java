package org.karaffe.compiler.frontend.karaffe.visitor;

import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeBaseVisitor;
import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeParser;
import org.karaffe.compiler.frontend.karaffe.ast.api.Statement;

public class ClassDefMemberVisitor extends KaraffeBaseVisitor<Statement> {

    @Override
    public Statement visitClassDefMember(KaraffeParser.ClassDefMemberContext ctx) {
        Statement statement = null;
        if (ctx.mainMethodDef() != null) {
            MainMethodDefVisitor mainMethodDefVisitor = new MainMethodDefVisitor();
            statement = ctx.mainMethodDef().accept(mainMethodDefVisitor);
        } else {
            throw new IllegalStateException("mainMethodDef is null");
        }
        return statement;
    }
}
