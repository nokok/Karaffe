package org.karaffe.compiler.frontend.karaffe.visitor;

import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeBaseVisitor;
import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeParser;
import org.karaffe.compiler.frontend.karaffe.ast.api.Statement;

public class StatementVisitor extends KaraffeBaseVisitor<Statement> {

    @Override
    public Statement visitStatement(KaraffeParser.StatementContext ctx) {
        if (ctx.expr() != null) {
            ExprVisitor exprVisitor = new ExprVisitor();
            return ctx.expr().accept(exprVisitor);
        } else {
            throw new IllegalStateException("expr is null");
        }
    }
}
