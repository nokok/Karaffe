package org.karaffe.compiler.frontend.karaffe.visitor;

import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeBaseVisitor;
import org.karaffe.compiler.frontend.karaffe.antlrautogenerated.KaraffeParser;
import org.karaffe.compiler.frontend.karaffe.ast.statements.ClassDef;

public class ClassDefVisitor extends KaraffeBaseVisitor<ClassDef> {
    @Override
    public ClassDef visitClassDef(KaraffeParser.ClassDefContext ctx) {
        if (ctx.simpleClassDef() != null) {
            SimpleClassDefVisitor visitor = new SimpleClassDefVisitor();
            return ctx.simpleClassDef().accept(visitor);
        } else {
            throw new IllegalStateException("null simpleClassDef");
        }
    }
}
